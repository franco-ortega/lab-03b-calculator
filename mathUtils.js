export function addNumbers(addNumber1, addNumber2) {
    const addSum = Number(addNumber1) + Number(addNumber2);
    return addSum;
    }

export function subNumbers(subNumber1, subNumber2) {
    const subSum = Number(subNumber1) - Number(subNumber2);
    return subSum;
    }

export function multiplyNumbers(multiplyNumber1, multiplyNumber2) {
    const multiplySum = Number(multiplyNumber1) * Number(multiplyNumber2);
    return multiplySum;
    }    

export function divideNumbers(divideNumber1, divideNumber2) {
    const divideSum = Number(divideNumber1) / Number(divideNumber2);
    return divideSum;
    }
        

